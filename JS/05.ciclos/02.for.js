// CICLO FOR

// el ciclo for se compone de tres partes: la inicialización, la condición y la actualización

for (let contador = 0; contador < 5; contador++) {
    console.log('Contador en ' + contador);
}

console.log('Fin del ciclo For');   

// el ciclo for puede manejar múltiples variables separadas por comas

for (let i = 0, j = 10; i < 5; i++, j--) {
    console.log('Var i: ' + i + ', Var j: ' + j);
}

console.log('Fin del ciclo For con múltiples variables');   

