// OPERADORES DE ASIGNACION

// BASICO, El operador de asignación básico es =
// en este caso asignamos el valor literal 1 a la variable a
let a = 1;

// COMPUESTO
// en este caso no estamos asignando al valor literal, sino le estamos asignando un incremento de 3
// es equivalente a hacer a = a + 3;
a += 3;
console.log(a);

// equivalente a hacer a = a - 2;
a -= 2;
console.log(a);

