// sentencia de control Switch

let numero = 3, numeroTexto = 'Valor desconocido';

switch(numero){
    case 1:
        numeroTexto = "Número uno";
        break;
    case 2:
        numeroTexto = "Número dos";
        break;
    case 3:
        numeroTexto = "Número tres";
        break;
    default:
        numeroTexto = "Número desconocido";
}

console.log(numeroTexto);

